       Savez-vous ce qu'est un nom de domaine ?<br/>
Il s'agit en fait d'une adresse sur le Web : octopus.fr est par exemple un nom de domaine.<br/>

Un nom de domaine est constitué de deux parties :  <font style color="red">exemple</font><font style color="blue">.com</font>.<br/><br/>

En rouge, le nom de domaine proprement dit. Il s'agit d'un nom que l'on peut en général choisir librement, tant que personne ne l'a réservé avant nous. Il peut contenir des lettres et des chiffres, mais pas de symboles particuliers (comme le « ç » français, le « é », le « è », les espaces, etc.).<br/><br/>

En bleu, l'extension (aussi appelée « TLD », de l'anglais top-level domain). Il existe grosso modo une extension par pays (.fr pour la France,.be pour la Belgique,.capour le Canada, etc.). Toutefois, il y a aussi des extensions utilisées au niveau international comme.com,.net,.org. Elles étaient au départ réservées aux sites commerciaux, aux organisations, … mais cela fait longtemps que tout le monde peut les réserver. D'ailleurs,.comest très probablement l'extension la plus utilisée sur le Web.<br/>

